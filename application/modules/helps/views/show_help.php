<h2 class='help expandable'><a href="#">Request Instalasi</a></h2>
<div class='categoryitems'>
TS memiliki <i>user interface</i> form instalasi yang berbeda dengan <strong>Sales</strong>, diantaranya adalah bahwa TS tidak dapat
mengganti Perusahaan <strong>Klien</strong>, <strong>Service</strong>, dan TS dapat mengisi <strong>Surveyor</strong>. Pada form TS juga tidak terdapat tombol
untuk menambah Request Instalasi, Menghapus dan Refresh.
Data yang harus diisikan <strong>Sales</strong> ketika request Instalasi adalah :
<ul>
<li>Nama Klien</li>
<li>Layanan</li>
<li>Tanggal Instalasi</li>
</ul>

Data yang harus dilengkapi oleh TS adalah:
<ul>
<li>Konfirmasi TS</li>
<li>Pelaksana Instalasi</li>
<li>Surat Ijin</li>
</ul>
