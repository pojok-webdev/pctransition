<h2>TS Melakukan Survey</h2>

<ol>
<li><strong>TS</strong> mendapatkan informasi adanya keperluan survey pada <strong>klien</strong>.</li>
<li><strong>TS</strong> memeriksa jadwal apakah bisa dilaksanakan pada tanggal yang ditentukan ataukah tidak.</li>
<li>Apabila diperlukan <strong>reschedule</strong>, maka <strong>TS</strong> mengajukan reschedule kepada <strong>Sales</strong>, 
untuk selanjutnya <strong>Sales</strong> bernegosiasi dengan <strong>klien</strong> guna mendapatkan waktu yang tepat.</li>
<li>Ketika tanggal yang tepat sudah ditentukan, <strong>TS</strong> mencatat, nama-nama Pelaksana Survey,
memeriksa surat ijin,</li>
<li>Setelah Survey, <strong>TS</strong> harus memberikan hasil Survey, di antaranya adalah:
<ol>
<li>Lokasi</li>
<li>Jarak dengan BTS</li>
<li>Jarak antar Pelanggan</li>
<li>Foto</li>
<li>Material yang diperlukan</li>
<li>Peralatan yang diperlukan</li>
</ol>
</li>
<li>Selanjutnya <strong>TS</strong> membuat kesimpulan apakah Instalasi dapat dilakukan pada <strong>klien</strong> tersebut</li>
<li>Hasil dari Survey dikirimkan kepada Team <strong>Sales</strong></li>
</ol>
